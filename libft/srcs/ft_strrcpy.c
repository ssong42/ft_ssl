#include "libft.h"

char	*ft_strrcpy(char *dest, char *src, int i, int a)
{
	if (a > i)
		return (0);
	while (a >= 0)
	{
		dest[i] = src[a];
		i--;
		a--;
	}
	return (dest);
}
